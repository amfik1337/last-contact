package net.emphased.lastcontact;

import java.io.IOException;

/**
 *
 */
public class CookieFileStoreException extends IOException
{
    public CookieFileStoreException()
    {
        super();
    }

    public CookieFileStoreException(String arg0, Throwable arg1)
    {
        super(arg0, arg1);
    }

    public CookieFileStoreException(String arg0)
    {
        super(arg0);
    }

    public CookieFileStoreException(Throwable arg0)
    {
        super(arg0);
    }

    private static final long serialVersionUID = -5311388042137120806L;
}
